package com.test;

public class Employee {
	private int id;
	private String name;
	private int salary;
	private String cmp;
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getSalary() {
		return salary;
	}
	public String getCmp() {
		return cmp;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public void setCmp(String cmp) {
		this.cmp = cmp;
	}

}
